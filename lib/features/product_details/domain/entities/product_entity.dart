class ProductEntity {
  final String id;
  final String name;
  final String description;
  final String price;
  final String image;
  final String rating;
  final String category;
  final String prepTime;

  ProductEntity({
    required this.id,
    required this.name,
    required this.description,
    required this.price,
    required this.image,
    required this.rating,
    required this.category,
    required this.prepTime,
  });
}
